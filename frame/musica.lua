notaAtual	= 1				-- nota atual representado pelo número da linha a partir da matriz "notas"

-- Método que retorna a próxima nota
-- Não atualiza a "notaAtual"
function proximaNota()
	return notas[notaAtual + 1];
end

-- Método que atualiza a notaAtual
function atualizaNota()
	notaAtual = notaAtual + 1
end

-- Método que atualiza a notaAtual para uma nota específica
function pulaNota(n)
	notaAtual = n
end

-- Método que re-inicializa para a primeira nota
function restart()
	notaAtual = 1
end
